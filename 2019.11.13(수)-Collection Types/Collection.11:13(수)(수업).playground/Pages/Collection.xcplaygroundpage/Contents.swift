/*
 Three primary collection types, known as arrays, sets, and dictionaries, for storing collections of values.
 
 Array - ordered collections of values.                         //순서가 있는 값들의 모음 중복가능
 Set - unordered collections of unique values.                  //순서가 없고 중복되지 않는다.
 Dictionary - unordered collections of key-value associations.  //키 밸류에 연관되고 순서가 없다.
